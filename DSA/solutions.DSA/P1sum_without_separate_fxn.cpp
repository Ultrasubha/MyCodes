//sum of two numbers as a basic program in c++
#include<iostream>
using namespace std;
 main()
{
	//static approach
	int a=90;
	int b=89;
	int c=a+b;
	cout<<"the ans is"<<c<<endl;  //endl is used for next next line
	
	//dynamic approach
	int x,y;
	cout<<"enter x and y";
	cin>>x>>y;
	int z=x+y;
	cout<<"the result is"<<z;	
}

