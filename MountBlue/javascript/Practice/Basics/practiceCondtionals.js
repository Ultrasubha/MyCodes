/*Conditional statements*/

// if..else
if(""==0){
    console.log("Empty String is falsy");
}
else {
    console.log("Nothing to print");
}

// switch
switch(5){
    case 1:
        console.log("1");
    case 2:
        console.log("2");
    default:
        console.log("default");
}

// ternary operator ?
console.log((5<1)?true:false);